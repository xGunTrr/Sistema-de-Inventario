-- ============================================
-- PROVEEDOR
-- ============================================

CREATE TABLE Proveedor (
    IdProveedor INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NombreProveedor VARCHAR(255) NOT NULL,
    DireccionProveedor VARCHAR(500),
    Telefono VARCHAR(20),
    Email VARCHAR(100),
    Activo BOOLEAN NOT NULL DEFAULT TRUE,
    FechaCreacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FechaModificacion TIMESTAMP
);

-- ============================================
-- PRODUCTO
-- ============================================

CREATE TABLE Producto (
    IdProducto INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IdProveedor INTEGER NOT NULL,
    CodigoProducto VARCHAR(50),
    CodigoBarras VARCHAR(100),
    NombreProducto VARCHAR(255) NOT NULL,
    DescripcionProducto TEXT,
    CategoriaProducto VARCHAR(100),
    PrecioCompra NUMERIC(10,2) NOT NULL DEFAULT 0,
    PrecioVenta NUMERIC(10,2) NOT NULL DEFAULT 0,
    CantidadReorden INTEGER DEFAULT 0,
    PesoEmpaque NUMERIC(10,2),
    AltoEmpaque NUMERIC(10,2),
    AnchoEmpaque NUMERIC(10,2),
    ProfundidadEmpaque NUMERIC(10,2),
    Refrigerado BOOLEAN DEFAULT FALSE,
    Activo BOOLEAN NOT NULL DEFAULT TRUE,
    FechaCreacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FechaModificacion TIMESTAMP,

    CONSTRAINT fk_producto_proveedor
        FOREIGN KEY (IdProveedor)
        REFERENCES Proveedor(IdProveedor)
);

-- ============================================
-- CLIENTE
-- ============================================

CREATE TABLE Cliente (
    IdCliente INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NombreCliente VARCHAR(255) NOT NULL,
    DireccionCliente VARCHAR(500),
    Telefono VARCHAR(20),
    Email VARCHAR(100),
    Activo BOOLEAN NOT NULL DEFAULT TRUE,
    FechaCreacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FechaModificacion TIMESTAMP
);

-- ============================================
-- UBICACION
-- ============================================

CREATE TABLE Ubicacion (
    IdUbicacion INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NombreUbicacion VARCHAR(255) NOT NULL,
    DireccionUbicacion VARCHAR(500),
    Ciudad VARCHAR(100),
    Referencia VARCHAR(255),
    TelefonoContacto VARCHAR(20),
    Activo BOOLEAN NOT NULL DEFAULT TRUE,
    FechaCreacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FechaModificacion TIMESTAMP
);

-- ============================================
-- ALMACEN
-- ============================================

CREATE TABLE Almacen (
    IdAlmacen INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IdUbicacion INTEGER NOT NULL,
    NombreAlmacen VARCHAR(255) NOT NULL,
    EsRefrigerado BOOLEAN DEFAULT FALSE,
    Activo BOOLEAN NOT NULL DEFAULT TRUE,
    FechaCreacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FechaModificacion TIMESTAMP,

    CONSTRAINT fk_almacen_ubicacion
        FOREIGN KEY (IdUbicacion)
        REFERENCES Ubicacion(IdUbicacion)
);

-- ============================================
-- PEDIDO (con IdAlmacen para saber donde recibe)
-- ============================================

CREATE TABLE Pedido (
    IdPedido INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IdProveedor INTEGER NOT NULL,
    IdAlmacen INTEGER,
    FechaPedido TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    Estado VARCHAR(30) NOT NULL DEFAULT 'Pendiente',
    Activo BOOLEAN NOT NULL DEFAULT TRUE,
    FechaCreacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FechaModificacion TIMESTAMP,

    CONSTRAINT fk_pedido_proveedor
        FOREIGN KEY (IdProveedor)
        REFERENCES Proveedor(IdProveedor),

    CONSTRAINT fk_pedido_almacen
        FOREIGN KEY (IdAlmacen)
        REFERENCES Almacen(IdAlmacen)
);

-- ============================================
-- DETALLE PEDIDO (con CantidadRecibida para recepcion parcial)
-- ============================================

CREATE TABLE DetallePedido (
    IdDetallePedido INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IdPedido INTEGER NOT NULL,
    IdProducto INTEGER NOT NULL,
    Cantidad INTEGER NOT NULL,
    CantidadRecibida INTEGER DEFAULT 0,
    PrecioUnitario NUMERIC(10,2) NOT NULL,
    Activo BOOLEAN NOT NULL DEFAULT TRUE,
    FechaCreacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FechaModificacion TIMESTAMP,

    CONSTRAINT fk_detpedido_pedido
        FOREIGN KEY (IdPedido)
        REFERENCES Pedido(IdPedido),

    CONSTRAINT fk_detpedido_producto
        FOREIGN KEY (IdProducto)
        REFERENCES Producto(IdProducto)
);

-- ============================================
-- ENTREGA (con IdAlmacen para saber de donde despacha)
-- ============================================

CREATE TABLE Entrega (
    IdEntrega INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IdCliente INTEGER NOT NULL,
    IdPedido INTEGER,
    IdAlmacen INTEGER,
    FechaVenta TIMESTAMP,
    FechaEsperada DATE,
    FechaReal DATE,
    Estado VARCHAR(30) NOT NULL DEFAULT 'Pendiente',
    Activo BOOLEAN NOT NULL DEFAULT TRUE,
    FechaCreacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FechaModificacion TIMESTAMP,

    CONSTRAINT fk_entrega_cliente
        FOREIGN KEY (IdCliente)
        REFERENCES Cliente(IdCliente),

    CONSTRAINT fk_entrega_pedido
        FOREIGN KEY (IdPedido)
        REFERENCES Pedido(IdPedido),

    CONSTRAINT fk_entrega_almacen
        FOREIGN KEY (IdAlmacen)
        REFERENCES Almacen(IdAlmacen)
);

-- ============================================
-- DETALLE ENTREGA
-- ============================================

CREATE TABLE DetalleEntrega (
    IdDetalleEntrega INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IdEntrega INTEGER NOT NULL,
    IdProducto INTEGER NOT NULL,
    CantidadEntregada INTEGER NOT NULL,
    PrecioUnitario NUMERIC(10,2) NOT NULL,
    Activo BOOLEAN NOT NULL DEFAULT TRUE,
    FechaCreacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FechaModificacion TIMESTAMP,

    CONSTRAINT fk_detentrega_entrega
        FOREIGN KEY (IdEntrega)
        REFERENCES Entrega(IdEntrega),

    CONSTRAINT fk_detentrega_producto
        FOREIGN KEY (IdProducto)
        REFERENCES Producto(IdProducto)
);

-- ============================================
-- INVENTARIO
-- ============================================

CREATE TABLE Inventario (
    IdInventario INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IdProducto INTEGER NOT NULL,
    IdAlmacen INTEGER NOT NULL,
    CantidadDisponible INTEGER DEFAULT 0,
    StockMinimo INTEGER DEFAULT 0,
    StockMaximo INTEGER DEFAULT 0,
    PuntoReorden INTEGER DEFAULT 0,
    Activo BOOLEAN NOT NULL DEFAULT TRUE,
    FechaCreacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FechaModificacion TIMESTAMP,

    CONSTRAINT uk_producto_almacen
        UNIQUE (IdProducto, IdAlmacen),

    CONSTRAINT fk_inventario_producto
        FOREIGN KEY (IdProducto)
        REFERENCES Producto(IdProducto),

    CONSTRAINT fk_inventario_almacen
        FOREIGN KEY (IdAlmacen)
        REFERENCES Almacen(IdAlmacen)
);

-- ============================================
-- TRANSFERENCIA
-- ============================================

CREATE TABLE Transferencia (
    IdTransferencia INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IdAlmacenOrigen INTEGER NOT NULL,
    IdAlmacenDestino INTEGER NOT NULL,
    CantidadTransferida INTEGER NOT NULL,
    FechaEnvio TIMESTAMP,
    FechaRecepcion TIMESTAMP,
    Estado VARCHAR(30) NOT NULL DEFAULT 'Pendiente',
    Activo BOOLEAN NOT NULL DEFAULT TRUE,
    FechaCreacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FechaModificacion TIMESTAMP,

    CONSTRAINT fk_transferencia_almacen_origen
        FOREIGN KEY (IdAlmacenOrigen)
        REFERENCES Almacen(IdAlmacen),

    CONSTRAINT fk_transferencia_almacen_destino
        FOREIGN KEY (IdAlmacenDestino)
        REFERENCES Almacen(IdAlmacen)
);

-- ============================================
-- DETALLE TRANSFERENCIA (nueva tabla)
-- ============================================

CREATE TABLE DetalleTransferencia (
    IdDetalleTransferencia INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IdTransferencia INTEGER NOT NULL,
    IdProducto INTEGER NOT NULL,
    CantidadTransferida INTEGER NOT NULL DEFAULT 0,
    Activo BOOLEAN NOT NULL DEFAULT TRUE,
    FechaCreacion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FechaModificacion TIMESTAMP,

    CONSTRAINT fk_dettransf_transferencia
        FOREIGN KEY (IdTransferencia)
        REFERENCES Transferencia(IdTransferencia),

    CONSTRAINT fk_dettransf_producto
        FOREIGN KEY (IdProducto)
        REFERENCES Producto(IdProducto)
);


